package com.idata365.app.entity;

public class ScoreByDayResultBean
{
	private String score;
	
	private String speedingState;
	
	private String driveState;
	
	private String creditState;
	
	private String steadyState;
	
	private String mileageState;

	public String getScore()
	{
		return score;
	}

	public void setScore(String score)
	{
		this.score = score;
	}

	public String getSpeedingState()
	{
		return speedingState;
	}

	public void setSpeedingState(String speedingState)
	{
		this.speedingState = speedingState;
	}

	public String getDriveState()
	{
		return driveState;
	}

	public void setDriveState(String driveState)
	{
		this.driveState = driveState;
	}

	public String getCreditState()
	{
		return creditState;
	}

	public void setCreditState(String creditState)
	{
		this.creditState = creditState;
	}

	public String getSteadyState()
	{
		return steadyState;
	}

	public void setSteadyState(String steadyState)
	{
		this.steadyState = steadyState;
	}

	public String getMileageState()
	{
		return mileageState;
	}

	public void setMileageState(String mileageState)
	{
		this.mileageState = mileageState;
	}
	
}
